/*
Problem Name: Bitwise OR of Two Integers
Problem Statement:
-----------------
Write a Java program to perform a bitwise OR operation between two integers 
and display the result.

The program should:
1. Take two integers as input (in this example, 5 and 6 are used directly).
2. Compute the bitwise OR of the two integers using the '|' operator.
3. Print the resulting integer.

Example:
Input: 5 (binary: 0101), 6 (binary: 0110)
Output: 7
Explanation: 0101 | 0110 = 0111, which is 7 in decimal.
*/


public class Solution{
    public static void main(String[] args) {
        System.out.println(5|6);
    }
}