/*
Reverse Right-Angled Triangle Pattern
-------------------------------------
Write a Java program to print a right-angled triangle pattern in reverse order 
using asterisks (*). The pattern should start with 5 stars in the first row and 
reduce by one star in each subsequent row.

Expected Output:
*****
****
***
**
*
*/

public class Pattern1 {

// *****
// ****
// ***
// **
// *

    public static void main(String[] args) {
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

    }
}
